import java.util.Scanner;

public class ClassicSum {
    public static void main(String[] args) {
        String greeting = "Hello, world (this is a variable)!";
        System.out.println(greeting);

        System.out.println("-----------------------");

        Integer a = 5;
        Integer b = 5;
        Integer sum = a + b;

        System.out.println("a: " + a + " and b: " + b);
        System.out.println("This will print the variable sum");
        System.out.println("So, a + b = " + sum);

        System.out.println("This will print the variable a plus the variable b");
        System.out.println("So, a + b = " + (a + b));
    } 
}
